/*************************************************************************************
 *  \Description     : Initializes Button pins
 ************************************************************************************/
 void BUTTON_Init(DIO_Channel_et buttonPin, DIO_Port_et buttonPort)
 {
    DIO_Init(buttonPin, buttonPort, INPUT);
 }

/*************************************************************************************
 *  \Description     : Reads button pins
 ************************************************************************************/
 void BUTTON_Read(DIO_Channel_et buttonPin, DIO_Port_et buttonPort, uint8 *readValue)
 {
    DIO_Read(buttonPin, buttonPort, readValue);
 }


