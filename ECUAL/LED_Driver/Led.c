#include "Led.h"

/* ================================================================================================================= */
/* ================                                GLOBAL FUNCTIONS                                 ================ */
/* ================================================================================================================= */

/*************************************************************************************
 *  \Description     : Initializes LED pins
 ************************************************************************************/
 void LED_Init(DIO_Channel_et ledPin, DIO_Port_et ledPort)
 {
    DIO_Init(ledPin, ledPort, OUTPUT);
 }

/*************************************************************************************
 *  \Description     : Turns LED ON
 ************************************************************************************/
 void LED_On(DIO_Channel_et ledPin, DIO_Port_et ledPort)
 {
    DIO_Write(ledPin, ledPort, HIGH);
 }

/*************************************************************************************
 *  \Description     : Turns LED OFF
 ************************************************************************************/
 void LED_Off(DIO_Channel_et ledPin, DIO_Port_et ledPort)
 {
    DIO_Write(ledPin, ledPort, LOW);
 }

/*************************************************************************************
 *  \Description     : Toggles LED
 ************************************************************************************/
 void LED_Toggle(DIO_Channel_et ledPin, DIO_Port_et ledPort)
 {
    DIO_Toggle(ledPin, ledPort);
 }


