#ifndef INTERRUPTS_CFG_H
#define INTERRUPTS_CFG_H

/* ================================================================================================================= */
/* ================                              GLOBAL CONSTANT MACROS                             ================ */
/* ================================================================================================================= */

/**********************************************************************************************************************
 *  Global Interrupts Enable/Disable:
 *  0 -> Disable        (Default)
 *  1 -> Enable
 *********************************************************************************************************************/
 #define GLOBAL_INTERRUPTS_ENABLE 1

/* <----------------------------------------[ External INT0 Configurations ]---------------------------------------> */

/**********************************************************************************************************************
 *  Enable/Disable INT0:
 *  0 -> Disable INT0        (Default)
 *  1 -> Enable INT0
 *********************************************************************************************************************/
 #define INT0_ENABLE 1

/**********************************************************************************************************************
 *  User Configurations for External Interrupts sense control:
 *  0 -> The low level of INT0 generates an interrupt request.        (Default)
 *  1 -> Any logical change on INT0 generates an interrupt request.
 *  2 -> The falling edge of INT0 generates an interrupt request.
 *  3 -> The rising edge of INT0 generates an interrupt request
 *********************************************************************************************************************/
 #define INT0_SENSE_CONTROL 2

 /* <----------------------------------------[ External INT1 Configurations ]---------------------------------------> */

/**********************************************************************************************************************
 *  Enable/Disable INT1:
 *  0 -> Disable INT1        (Default)
 *  1 -> Enable INT1
 *********************************************************************************************************************/
 #define INT1_ENABLE 0

 /**********************************************************************************************************************
 *  User Configurations for External Interrupts sense control:
 *  0 -> The low level of INT0 generates an interrupt request.        (Default)
 *  1 -> Any logical change on INT0 generates an interrupt request.
 *  2 -> The falling edge of INT0 generates an interrupt request.
 *  3 -> The rising edge of INT0 generates an interrupt request
 *********************************************************************************************************************/
 #define INT1_SENSE_CONTROL 3

 /* <----------------------------------------[ External INT2 Configurations ]---------------------------------------> */

/**********************************************************************************************************************
 *  Enable/Disable INT2:
 *  0 -> Disable INT2        (Default)
 *  1 -> Enable INT2
 *********************************************************************************************************************/
 #define INT2_ENABLE 0

 /**********************************************************************************************************************
 *  User Configurations for INT2 sense control:
 *  0 -> The falling edge of INT2 generates an interrupt request.       (Default)
 *  1 -> The rising edge of INT2 generates an interrupt request
 *********************************************************************************************************************/
 #define  INT2_SENSE_CONTROL 1

#endif /* INTERRUPTS_CFG_H */

